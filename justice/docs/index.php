                <h2>Project Overview</h2>
                <p>Justice Society is a user-friendly platform designed to effortlessly connect users with lawyers, providing a seamless experience to access a wide range of legal services without any inconvenience. The project includes several features such as User registration and login, Lawyer profile creation and management, Search functionality for finding lawyers by name, Appointment scheduling, and Admin dashboard for managing users and lawyers.</p>
